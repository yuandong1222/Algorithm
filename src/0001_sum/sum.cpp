#include "problem.h"

#include <iostream>

using namespace algorithm;
using namespace std;

void Problem::SetUp(istream& input, ostream& output) {}
void Problem::TearDown(istream& input, ostream& output) {}

void Problem::Run(istream& input, ostream& output) {
	int a = 0, b = 0;

	input >> a >> b;
	while (a != 0 || b != 0) {
		output << a + b << endl;
		input >> a >> b;
	}

}

